def format_colour(colour_string):
    """
    Takes a string representing a colour and leaves both hex-codes and HTML names as strings,
    but converts RGB values to a list of ints

    :param colour_string: Colour in hex-code, RGB values or HTML name format
    :type colour_string: str
    :return: hex-code, RGB values or HTML name and the format
    :rtype: tuple[str | list[int], str]
    """

    colour_string = colour_string.lower()
    if colour_string.count(" ") == 2:
        colour_value = colour_string.split()
        colour_value = [int(colour_value[i]) for i in range(3)]
        colour_format = "RGB values"
    elif colour_string in html_code_lookup:
        colour_value = colour_string
        colour_format = "html_name"
    else:
        colour_value = colour_string
        colour_format = "hex-code"

    return colour_value, colour_format


def hex_to_rgb(hex_code):
    """
    Converts a colour's hex-code as a string to a list containing it's RGB values

    :param hex_code: hex-code
    :type hex_code: str
    :return: RGB values
    :rtype: list[int]
    """

    hex_list = list(hex_code.lower())
    rgb_list = []

    for i in range(3):
        rgb_list.append(f"0x{hex_list[2 * i]}{hex_list[2 * i + 1]}")
    rgb_values = [int(rgb_list[i], 16) for i in range(3)]

    return rgb_values


def rgb_to_hex(rgb_values):
    """
    Converts a colour's RGB values in a list to a string with it's hex code

    :param rgb_values: RGB values
    :type rgb_values: list[int]
    :return: hex code
    :rtype: str
    """

    hex_code = ""

    for i in range(3):
        value = hex(rgb_values[i])
        hex_code += value[2:]

    return hex_code


def html_converter(html_colour_name):
    """
    Converts a HTML colour name to the hex-code and RGB values it represents

    :param html_colour_name: Colour name used by HTML
    :type html_colour_name: str
    :return: Both hex-code and RGB values
    :rtype: tuple[str, list[int]]
    """

    hex_code = html_code_lookup[html_colour_name][0]
    rgb_values = html_code_lookup[html_colour_name][1]

    return hex_code, rgb_values


def html_name_finder(hex_code, rgb_values):
    """
    Returns the name HTML uses for a colour code if it has such a name

    :param hex_code: hex-code
    :type hex_code: str
    :param rgb_values: RGB values
    :type rgb_values: list[int]
    :return: HTML colour name if exists, None otherwise
    :rtype: str | None
    """

    colour_tuple = (hex_code, rgb_values)
    dict_lookup = (html for html, values in html_code_lookup.items() if values == colour_tuple)
    html_name = next(dict_lookup, None)

    return html_name


class Colour:
    """
    Creates a colour object used for the colouring of the canvas and any shapes on it
    """

    hex_default = "000000"
    rgb_default = (0, 0, 0)
    html_default = ""

    def __init__(self, colour_string):
        """
        :param colour_string: hex-code, RGB values or HTML name of colour as string
        :type colour_string: str
        """

        colour_value, colour_format = format_colour(colour_string)
        if colour_format == "hex-code":
            self.hex_code = colour_value
            self.rgb_values = hex_to_rgb(self.hex_code)
            self.html_name = html_name_finder(self.hex_code, self.rgb_values)
        elif colour_format == "RGB values":
            self.rgb_values = colour_value
            self.hex_code = rgb_to_hex(self.rgb_values)
            self.html_name = html_name_finder(self.hex_code, self.rgb_values)
        else:
            self.html_name = colour_value
            self.hex_code, self.rgb_values = html_converter(self.html_name)
